package com.Przyklad;

public class Target {
    double targetWidth;
    double bullseyeRadius;
    int minBullseyePoint;
    double ringDistance;
    Target(double targetWidth, double bullseyeRadius, int minBullseyePoint, double ringDistance)
    {
        this.targetWidth = targetWidth;
        this.bullseyeRadius = bullseyeRadius;
        this.minBullseyePoint = minBullseyePoint;
        this.ringDistance = ringDistance;
    }
}
